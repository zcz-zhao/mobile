% X1-子系统
sys(4) = -x(1)*z1/(k_b1^2-z1^2)-r_phi1*x(4); % phi1

for i=1:1:nd_j1
    sys(4+i) = -r_c1*S_j1_2(i,:)*W_c1; % W_c1
end

for i=1:1:nd_j1
    sys(4+nd_j1+i) = -S_j1_2(i,:)*(r_a1*(W_a1-W_c1)+r_c1*W_c1); % W_a1
